CREATE table users (
    name VARCHAR,
    password VARCHAR,
    PRIMARY KEY (name)
);
CREATE table students (
    id VARCHAR,
    name VARCHAR,
    photo VARCHAR,
    PRIMARY KEY (id)
);
CREATE table class (
    username VARCHAR,
    classname VARCHAR,
    student VARCHAR,
    PRIMARY KEY (username, classname, student),
    FOREIGN KEY (student) REFERENCES students(id),
    FOREIGN KEY (username) references users(name)
);
CREATE INDEX cls ON class(classname);
CREATE INDEX student_to_class ON class(student);


CREATE table photo (
    username VARCHAR,
    classname VARCHAR,
    photo VARCHAR,
    date VARCHAR,
    PRIMARY KEY (username, classname, date)
);

create table attendance (
    username VARCHAR,
    classname VARCHAR,
    date VARCHAR,
    student VARCHAR,
    attendance boolean,
    PRIMARY KEY (username, classname, date, student),
    FOREIGN KEY (username) references users(name),
    FOREIGN KEY (username, classname, student) references class(username, classname, student),
    FOREIGN KEY (username, classname, date) references photo(username, classname, date)
);
